// 变量
// 常量
// 可变性
// 变量遮蔽

fn main() {
    // 变量是一种绑定语义
    // 将一个变量名与一个值绑定在一起
    // 变量默认不可变(第一次赋值后, x变成只读状态)
    let x: u8 = 100;
    println!("x不可变: {}", x);

    // 可变变量(第一次赋值后, y变为可写状态)
    let mut y = 100;
    y = 200;
    println!("y可变: {}", y);
    
    // 变量遮蔽
    let z: u8 = 100;
    let z: u8 = 200;
    println!("变量遮蔽: {}", z);

   // 常量
   const MAX: u64 = 1000;
   println!("常量: {}", MAX)
}
